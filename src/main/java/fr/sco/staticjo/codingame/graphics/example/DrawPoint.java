package fr.sco.staticjo.codingame.graphics.example;

public class DrawPoint {

	private int x;
	private int y;
	private int id;
	
	public DrawPoint(int xx, int yy, int id){
		x = xx;
		y = yy;
		this.id = id;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
